<!-- FAQ Toggle Script -->
<script>
    function toggleFAQ(button) {
        const faqItem = button.parentElement;
        const content = faqItem.querySelector('.faq-content');
        const icon = button.querySelector('.faq-icon');
        const isOpen = content.style.maxHeight && content.style.maxHeight !== '0px';
        
        // Close all other FAQs
        document.querySelectorAll('.faq-item').forEach(item => {
            if (item !== faqItem) {
                const otherContent = item.querySelector('.faq-content');
                const otherIcon = item.querySelector('.faq-icon');
                otherContent.style.maxHeight = '0px';
                otherIcon.style.transform = 'rotate(0deg)';
                otherIcon.querySelector('svg').innerHTML = '<path stroke-linecap="round" stroke-linejoin="round" stroke-width="2" d="M12 6v12m6-6H6" />';
            }
        });
        
        // Toggle current FAQ
        if (isOpen) {
            content.style.maxHeight = '0px';
            icon.style.transform = 'rotate(0deg)';
            icon.querySelector('svg').innerHTML = '<path stroke-linecap="round" stroke-linejoin="round" stroke-width="2" d="M12 6v12m6-6H6" />';
        } else {
            content.style.maxHeight = content.scrollHeight + 'px';
            icon.style.transform = 'rotate(180deg)';
            icon.querySelector('svg').innerHTML = '<path stroke-linecap="round" stroke-linejoin="round" stroke-width="2" d="M20 12H4" />';
        }
    }
</script>

