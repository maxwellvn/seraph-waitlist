                    const data = await response.json();
                    
                    if (data.success) {
                        // Success
                        messageDiv.innerText = data.message;
                        messageDiv.className = 'absolute top-full left-0 w-full mt-4 text-sm font-body text-green-600 opacity-100 transition-opacity duration-300';
                        emailInput.value = '';
                        
                        // Reset message after 5 seconds
                        setTimeout(() => {
                            messageDiv.classList.replace('opacity-100', 'opacity-0');
                        }, 5000);
                        
                    } else {
                        // Error from server
                        throw new Error(data.message || 'Something went wrong. Please try again.');
                    }
                    
                } catch (error) {
                    // Display error
                    messageDiv.innerText = error.message;
                    messageDiv.className = 'absolute top-full left-0 w-full mt-4 text-sm font-body text-red-500 opacity-100 transition-opacity duration-300';
                } finally {
                    // Reset button
                    submitBtn.innerText = originalBtnText;
                    submitBtn.disabled = false;
                    submitBtn.classList.remove('opacity-70', 'cursor-not-allowed');
                }
            });
        }
    });
</script>
