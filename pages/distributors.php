<!-- Form Submission Script -->
<script>
document.addEventListener('DOMContentLoaded', function() {
    const form = document.getElementById('distributorForm');
    const submitBtn = document.getElementById('submitBtn');
    const formMessage = document.getElementById('formMessage');

    if (form) {
        form.addEventListener('submit', async function(e) {
            e.preventDefault();

            // Get form data
            const formData = {
                fullName: document.getElementById('fullName').value.trim(),
                email: document.getElementById('email').value.trim(),
                phone: document.getElementById('phone').value.trim(),
                location: document.getElementById('location').value.trim(),
                businessType: document.getElementById('businessType').value,
                message: document.getElementById('message').value.trim()
            };

            // Basic validation
            if (!formData.fullName || !formData.email || !formData.phone || !formData.location) {
                showMessage('Please fill in all required fields.', 'error');
                return;
            }

            // Email validation
            const emailRegex = /^[^\s@]+@[^\s@]+\.[^\s@]+$/;
            if (!emailRegex.test(formData.email)) {
                showMessage('Please enter a valid email address.', 'error');
                return;
            }

            // Loading state
            const originalBtnText = submitBtn.innerText;
            submitBtn.innerText = 'Submitting...';
            submitBtn.disabled = true;
            submitBtn.classList.add('opacity-70', 'cursor-not-allowed');

            try {
                const response = await fetch('<?php echo BASE_URL; ?>api/distributor-application', {
                    method: 'POST',
                    headers: {
                        'Content-Type': 'application/json',
                        'Accept': 'application/json'
                    },
                    body: JSON.stringify(formData)
                });

                const data = await response.json();

                if (data.success) {
                    showMessage('Thank you for your application! We will be in touch soon.', 'success');
                    form.reset();
                } else {
                    showMessage(data.message || 'Something went wrong. Please try again.', 'error');
                }
            } catch (error) {
                showMessage('Unable to submit application. Please try again or contact us directly.', 'error');
            } finally {
                submitBtn.innerText = originalBtnText;
                submitBtn.disabled = false;
                submitBtn.classList.remove('opacity-70', 'cursor-not-allowed');
            }
        });
    }

    function showMessage(message, type) {
        formMessage.innerText = message;
        formMessage.classList.remove('hidden', 'text-green-600', 'text-red-500');
        formMessage.classList.add(type === 'success' ? 'text-green-600' : 'text-red-500');

        // Hide message after 5 seconds
        setTimeout(() => {
            formMessage.classList.add('hidden');
        }, 5000);
    }
});
</script>
